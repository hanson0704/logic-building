'''Reverse an Array
Reverse the order of elements in the given array.
Input: [1, 2, 3, 4, 5]
Output: [5, 4, 3, 2, 1]
'''

nums = [1, 2, 3, 4, 5]
reversed_array = nums[::-1]
print(nums)
print(reversed_array)

